export const getTranslation = (language, key) => {
  return translations[language]?.[key] || translations[DEFAULT_LANGUAGE][key] || key;
};

export const getPaymentTranslation = (language, method) => {
  return (
    translations[language]?.methods?.[method] ||
    translations[DEFAULT_LANGUAGE].methods?.[method] ||
    method
  );
};

export const getCategoryTranslation = (language, category) => {
  return categoryTranslations[category]?.[language] || category;
};

export const getProductTranslation = (language, product) => {
  const translatedProduct = productTranslations[product.name]?.[language];

  return {
    ...product,
    translatedName: translatedProduct?.name || product.name,
    translatedDescription: translatedProduct?.description || product.description,
    translatedCategory: getCategoryTranslation(language, product.category)
  };
};